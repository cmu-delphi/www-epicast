  <p><font size="5"> 2. Why did you choose to forecast "% of doctor
    visits that involve flu-like symptoms"?  Is this the only measure
    you are interested in?
  </font></p>


  <p>
    Answer: This is the measure CDC asked us to forecast first.  An
    elaborate system for measuring it already exists, and is being
    re-purposed to measure covid-19 levels.  We will likely also be
    asked to forecast measures related to covid-19 hospitalization.
  </p>
  </br>


  <p><font size="5"> 3. "This seems like the wrong measure - I think
  many COVID-19 patients may be diagnosed over the phone, and many
  routine visits may be cancelled during this period.”
  </font></p>

  <p>
    Answer: In that case, you should factor these beliefs into your
    forecasts.
  </p>
</br>

  <p><font size="5"> 4. “I think the best thing to nowcast would be
